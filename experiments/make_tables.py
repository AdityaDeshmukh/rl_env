"""Aggregate results_main/*.json (+ *.csv curves) across seeds and emit:
  - results_main/summary.txt   (human-readable)
  - results.tex                (LaTeX table, booktabs)
Metrics per (env, variant):
  final  : deterministic eval return at end of training (mean +/- std over seeds)
  best   : best eval return seen during training
  auc    : mean eval return over the whole curve (normalized 0..1 per env), i.e. sample efficiency
  solve  : env-steps to first reach a solve threshold (lower=better; '--' if never)
"""
import os, sys, json, glob, csv, math
from collections import defaultdict

# results dir (default: the classic-control main matrix); override via argv[1]
RES = sys.argv[1] if len(sys.argv) > 1 else "results/results_main"
TEX = sys.argv[2] if len(sys.argv) > 2 else "results.tex"
# solve thresholds (CartPole: near-max; Acrobot: strong policy)
SOLVE = {"CartPole-v1": 475.0, "Acrobot-v1": -110.0}
# display order + pretty names
VARIANT_ORDER = ["grpo", "rrebel_l1_std", "rrebel_huber_std", "rrebel_sq_std", "rrebel_l1_none"]
PRETTY = {
    "grpo":             r"GRPO",
    "rrebel_l1_std":    r"R-REBEL ($\ell_1$, std)",
    "rrebel_huber_std": r"R-REBEL (Huber, std)",
    "rrebel_sq_std":    r"R-REBEL (sq., std)",
    "rrebel_l1_none":   r"R-REBEL ($\ell_1$, no-std)",
}
ENV_ORDER = ["CartPole-v1", "Acrobot-v1"]


def parse_tag(tag):
    # "<env>__<variant>__s<seed>"
    env, variant, s = tag.split("__")
    return env, variant, int(s[1:])


def load():
    runs = defaultdict(list)  # (env,variant) -> list of dicts
    for jf in glob.glob(os.path.join(RES, "*.json")):
        d = json.load(open(jf))
        env, variant, seed = parse_tag(d["name"])
        # curve for auc / solve step
        cf = jf[:-5] + ".csv"
        curve = []
        if os.path.exists(cf):
            for row in csv.DictReader(open(cf)):
                curve.append((int(row["global_step"]), float(row["eval_mean"])))
        d["_curve"] = curve
        d["_env"], d["_variant"], d["_seed"] = env, variant, seed
        runs[(env, variant)].append(d)
    return runs


def mean_std(xs):
    xs = [x for x in xs if x is not None]
    if not xs:
        return float("nan"), float("nan")
    m = sum(xs) / len(xs)
    v = sum((x - m) ** 2 for x in xs) / max(1, len(xs) - 1)
    return m, math.sqrt(v)


def auc_norm(curve, env):
    """Mean eval over curve, normalized to [0,1] per env using rough return bounds."""
    if not curve:
        return None
    lo, hi = (0.0, 500.0) if env == "CartPole-v1" else (-500.0, -60.0)
    vals = [v for _, v in curve]
    m = sum(vals) / len(vals)
    return max(0.0, min(1.0, (m - lo) / (hi - lo)))


def solve_step(curve, env):
    thr = SOLVE[env]
    for step, v in curve:
        if v >= thr:
            return step
    return None


def summarize(runs):
    rows = {}
    for (env, variant), ds in runs.items():
        finals = [d["final_mean"] for d in ds]
        bests = [d["best_eval"] for d in ds]
        aucs = [auc_norm(d["_curve"], env) for d in ds]
        solves = [solve_step(d["_curve"], env) for d in ds]
        n_solved = sum(1 for s in solves if s is not None)
        solved_steps = [s for s in solves if s is not None]
        rows[(env, variant)] = dict(
            n=len(ds),
            final=mean_std(finals), best=mean_std(bests), auc=mean_std(aucs),
            n_solved=n_solved, solve=mean_std(solved_steps) if solved_steps else (None, None),
        )
    return rows


def fmt(m, s, nd=1):
    if m is None or (isinstance(m, float) and math.isnan(m)):
        return "--"
    return f"{m:.{nd}f} $\\pm$ {s:.{nd}f}"


def write_tex(rows, runs, path="results.tex"):
    # one budget/seed count assumed shared per env; grab from any run
    seeds = max((r["n"] for r in rows.values()), default=0)
    lines = []
    lines.append("% Auto-generated by make_tables.py")
    lines.append(r"% Requires: \usepackage{booktabs} \usepackage{multirow}")
    lines.append(r"\begin{table}[t]")
    lines.append(r"\centering")
    lines.append(r"\small")
    lines.append(r"\setlength{\tabcolsep}{5pt}")
    lines.append(r"\caption{R-REBEL vs.\ GRPO on classic-control Gym tasks (actor-only, group size $G{=}8$, "
                 r"one gradient step per group, matched learning rate, LR-annealed; identical rollout, "
                 r"architecture, step budget, and greedy evaluation). Deterministic eval return "
                 r"(mean $\pm$ std over %d seeds). \textbf{final}: return at end of training; "
                 r"\textbf{best}: best eval during training; \textbf{AUC}: mean eval over the whole run, "
                 r"normalized to $[0,1]$ (sample efficiency, higher is better); "
                 r"\textbf{solve@}: env-steps to first reach the solve threshold "
                 r"(CartPole $\geq 475$, Acrobot $\geq -110$; lower is better, $k$=thousand).}" % seeds)
    lines.append(r"\label{tab:rl-compare}")
    lines.append(r"\begin{tabular}{ll rrr r}")
    lines.append(r"\toprule")
    lines.append(r"Env & Method & final & best & AUC & solve@ \\")
    lines.append(r"\midrule")
    for env in ENV_ORDER:
        present = [v for v in VARIANT_ORDER if (env, v) in rows]
        for k, variant in enumerate(present):
            r = rows[(env, variant)]
            envcell = r"\multirow{%d}{*}{%s}" % (len(present), env.replace("-v1", "")) if k == 0 else ""
            solve_m, solve_s = r["solve"]
            if solve_m is None:
                solve_str = "--"
            else:
                solve_str = f"{solve_m/1000:.0f}k ({r['n_solved']}/{r['n']})"
            lines.append("%s & %s & %s & %s & %s & %s \\\\" % (
                envcell, PRETTY.get(variant, variant),
                fmt(*r["final"]), fmt(*r["best"]),
                fmt(*r["auc"], nd=3), solve_str))
        if env != ENV_ORDER[-1]:
            lines.append(r"\midrule")
    lines.append(r"\bottomrule")
    lines.append(r"\end{tabular}")
    lines.append(r"\end{table}")
    open(path, "w").write("\n".join(lines) + "\n")
    return path


def write_txt(rows, path=os.path.join(RES, "summary.txt")):
    L = []
    hdr = f"{'env':12s} {'variant':22s} {'n':>2s} {'final':>16s} {'best':>16s} {'AUC':>13s} {'solve@ (k steps)':>18s}"
    L.append(hdr); L.append("-" * len(hdr))
    for env in ENV_ORDER:
        for variant in VARIANT_ORDER:
            if (env, variant) not in rows:
                continue
            r = rows[(env, variant)]
            fm, fs = r["final"]; bm, bs = r["best"]; am, as_ = r["auc"]; sm, ss = r["solve"]
            solve = f"{sm/1000:.0f}k ({r['n_solved']}/{r['n']})" if sm is not None else f"-- (0/{r['n']})"
            L.append(f"{env:12s} {variant:22s} {r['n']:>2d} "
                     f"{fm:7.1f}+/-{fs:5.1f} {bm:7.1f}+/-{bs:5.1f} {am:6.3f}+/-{as_:5.3f} {solve:>18s}")
        L.append("")
    open(path, "w").write("\n".join(L) + "\n")
    print("\n".join(L))
    return path


if __name__ == "__main__":
    runs = load()
    if not runs:
        print("No results found in", RES); raise SystemExit
    rows = summarize(runs)
    write_txt(rows)
    p = write_tex(rows, runs, TEX)
    print("wrote", p)
