"""Summarize Atari single-objective results (results/atari/*.csv|json) into a
console table + LaTeX (atari_results.tex). Works on partial runs (reads CSV
curves directly, so it's useful before the job finishes)."""
import os, sys, json, glob, csv

RES = sys.argv[1] if len(sys.argv) > 1 else "results/atari"
TEX = sys.argv[2] if len(sys.argv) > 2 else "atari_results.tex"
PRETTY = {"grpo": "GRPO", "rrebel_sq_std": r"R-REBEL (sq.)",
          "rrebel_l1_std": r"R-REBEL ($\ell_1$)", "rrebel_huber_std": r"R-REBEL (Huber)"}
ORDER = ["grpo", "rrebel_sq_std", "rrebel_l1_std", "rrebel_huber_std"]


def parse_tag(tag):
    game, variant, seed = tag.split("__")
    return game, variant, seed


def load():
    rows = {}
    for cf in glob.glob(os.path.join(RES, "*.csv")):
        curve = [(int(r["global_step"]), float(r["eval_mean"])) for r in csv.DictReader(open(cf))]
        if not curve:
            continue
        tag = os.path.basename(cf)[:-4]
        game, variant, seed = parse_tag(tag)
        jf = cf[:-4] + ".json"
        final = json.load(open(jf))["final_mean"] if os.path.exists(jf) else curve[-1][1]
        rows[(game, variant)] = dict(curve=curve, final=final,
                                     best=max(v for _, v in curve),
                                     last_step=curve[-1][0], last=curve[-1][1])
    return rows


def main():
    rows = load()
    if not rows:
        print("no atari results yet in", RES); return
    games = sorted({g for g, _ in rows})
    hdr = f"{'game':10s} {'method':16s} {'steps':>9s} {'final/last':>11s} {'best':>7s}"
    print(hdr); print("-" * len(hdr))
    tex = [r"% auto-generated by atari_table.py  (requires \usepackage{booktabs})",
           r"\begin{table}[t]\centering\small",
           r"\caption{Single-objective Atari (Breakout), actor-only, $G{=}8$, entropy bonus, "
           r"async vector-env rollout. Greedy-eval game score; 1 seed (partial runs report "
           r"last-eval score at the reached step count).}",
           r"\label{tab:atari}", r"\begin{tabular}{ll rrr}", r"\toprule",
           r"Game & Method & steps & final/last & best \\", r"\midrule"]
    for g in games:
        vs = [v for v in ORDER if (g, v) in rows]
        for k, v in enumerate(vs):
            r = rows[(g, v)]
            gcell = g.split("/")[-1] if k == 0 else ""
            print(f"{gcell:10s} {v:16s} {r['last_step']:>9d} {r['final']:>11.2f} {r['best']:>7.2f}")
            tex.append(f"{gcell} & {PRETTY.get(v, v)} & {r['last_step']//1000}k & "
                       f"{r['final']:.2f} & {r['best']:.2f} \\\\")
    tex += [r"\bottomrule", r"\end{tabular}", r"\end{table}"]
    open(TEX, "w").write("\n".join(tex) + "\n")
    print("\nwrote", TEX)


if __name__ == "__main__":
    main()
